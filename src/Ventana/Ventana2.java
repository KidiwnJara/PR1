    private void registrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar1ActionPerformed
        /**
         * Ingresa Los datos en Pantalla y los Almacena en las Variables de la Clase
         * Evalua si algun espacio esta en blanco para que no pueda ingrsar nada si el espacio esta en blancoa
         */
        if(nombre1.getText().equals("") || apellidos1.getText().equals("")
                || id1.getText().equals("") || Fecha1.getText().equals("")
                || Contrato.getText().equals("") || rol.getText().equals("")
                || tel.getText().equals("") || email.getText().equals("")){
            
            JOptionPane.showMessageDialog(null,"Espacio Vacio \n Debe de llenar Todas los datos",null,
                                    JOptionPane.ERROR_MESSAGE);
            nombre1.setFocusable(true);
            
        }else{
        this.modelo.addRow(new Object [] {this.nombre1.getText(),this.apellidos1.getText(),
            this.tipo_id.getSelectedItem(),this.id1.getText(),this.Fecha1.getText(),
                this.Contrato.getText(),this.rol.getText(),this.tel.getText(),this.email.getText()});
        this.nombre1.setText("");
        this.apellidos1.setText("");
        this.tipo_id.setSelectedIndex(0);
        this.id1.setText("");
        this.Fecha1.setText("");
        this.Contrato.setText("");
        this.rol.setText("");
        this.tel.setText("");
        this.email.setText("");
        nombre1.grabFocus();
                    }
    }//GEN-LAST:event_registrar1ActionPerformed

